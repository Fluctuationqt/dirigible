package org.eclipse.dirigible.components.websockets.message;

/**
 * The Class InputMessage.
 */
public class InputMessage {

    /** The from. */
    private String from;
    
    /** The text. */
    private String text;

    /**
     * Gets the text.
     *
     * @return the text
     */
    public String getText() {
        return text;
    }

    /**
     * Gets the from.
     *
     * @return the from
     */
    public String getFrom() {
        return from;
    }
}
