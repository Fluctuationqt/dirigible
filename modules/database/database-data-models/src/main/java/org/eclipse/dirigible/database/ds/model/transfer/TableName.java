package org.eclipse.dirigible.database.ds.model.transfer;

/**
 * The Class TableName.
 */
public class TableName {

	/** The name. */
	private String name;

	/** The type. */
	private String type;

	/**
	 * Instantiates a new table name.
	 *
	 * @param name the name
	 * @param type the type
	 */
	public TableName(String name, String type) {
		super();
		this.name = name;
		this.type = type;
	}

	/**
	 * Gets the name.
	 *
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * Sets the name.
	 *
	 * @param name the new name
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * Gets the type.
	 *
	 * @return the type
	 */
	public String getType() {
		return type;
	}

	/**
	 * Sets the type.
	 *
	 * @param type the new type
	 */
	public void setType(String type) {
		this.type = type;
	}

}